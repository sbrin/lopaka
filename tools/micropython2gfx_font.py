# Transpose the bitmap data to convert from column-major to row-major format
transposed_bitmap = []
for i in range(0, len(bitmap_raw), 8):
    char_data = bitmap_raw[i:i+8]
    for row in range(8):
        byte = 0
        for col in range(8):
            if char_data[col] & (1 << row):
                byte |= (1 << (7 - col))  # Reverse the bit order by using (7 - col)
        transposed_bitmap.append(byte)

glyphs = []
bitmap_offset = 0
for i in range(char_count):
    glyphs.append(GFXglyph(
        bitmap_offset=bitmap_offset,
        width=8,
        height=8,
        xAdvance=8,
        xOffset=0,
        yOffset=0
    ))
    bitmap_offset += 8  # 8 bytes per glyph (8x8 monochrome)

# Output C header
def format_c_array(name, array, per_line=12):
    out = f"const uint8_t {name}[] PROGMEM = {{\n"
    lines = textwrap.wrap(', '.join(f"0x{b:02X}" for b in array), per_line * 6)
    for line in lines:
        out += "  " + line + "\n"
    out += "};\n"
    return out

def format_glyphs(glyphs):
    out = f"const GFXglyph Petme128Glyphs[] PROGMEM = {{\n"
    for g in glyphs:
        out += f"  {{ {g.bitmap_offset}, {g.width}, {g.height}, {g.xAdvance}, {g.xOffset}, {g.yOffset} }},\n"
    out += "};\n"
    return out

def format_font_struct(name, glyphs, bitmap_name, glyph_name, first, last, yAdvance):
    return f"""
const GFXfont {name} PROGMEM = {{
  (uint8_t*){bitmap_name},
  (GFXglyph*){glyph_name},
  0x{first:02X}, 0x{last:02X}, {yAdvance}
}};
"""

# Generate .h contents
bitmap_data = format_c_array("Petme128Bitmaps", transposed_bitmap)
glyph_data = format_glyphs(glyphs)
font_data = format_font_struct("Petme128Font", glyphs, "Petme128Bitmaps", "Petme128Glyphs", start_char, end_char, 8)

# Final output
with open("Petme128Font.h", "w") as f:
    f.write("#include <Adafruit_GFX.h>\n\n")
    f.write(bitmap_data + "\n")
    f.write(glyph_data + "\n")
    f.write(font_data)

